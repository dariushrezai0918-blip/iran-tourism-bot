from config import HASHTAGS

MAX_DESCRIPTION = 850


def short_text(text, length=MAX_DESCRIPTION):
    if not text:
        return ""

    text = text.replace("\n", " ")

    if len(text) <= length:
        return text

    return text[:length] + "..."


def format_post(info):

    description = short_text(info["description"])

    hashtags = "\n".join(HASHTAGS)

    post = f"""🌍 {info['title']}

📍 مقصد گردشگری

━━━━━━━━━━━━━━

🏛 معرفی

{description}

━━━━━━━━━━━━━━

📖 تاریخچه

این مکان یکی از شناخته‌شده‌ترین آثار تاریخی و گردشگری جهان محسوب می‌شود و در طول سال‌های مختلف نقش مهمی در تاریخ، فرهنگ و هویت منطقه خود داشته است. امروزه هزاران گردشگر برای بازدید از این جاذبه به این منطقه سفر می‌کنند.

━━━━━━━━━━━━━━

✨ نکات جالب

🔹 از مشهورترین جاذبه‌های گردشگری جهان است.

🔹 ارزش تاریخی و فرهنگی بسیار بالایی دارد.

🔹 سالانه گردشگران زیادی از کشورهای مختلف از آن بازدید می‌کنند.

🔹 یکی از بهترین مکان‌ها برای عکاسی و آشنایی با فرهنگ محلی است.

━━━━━━━━━━━━━━

🧳 راهنمای سفر

🗓 بهترین زمان بازدید:
بهار و اوایل پاییز

📸 مناسب برای:
گردشگری، عکاسی، علاقه‌مندان به تاریخ و فرهنگ

🎒 پیشنهاد:
حداقل چند ساعت برای بازدید کامل زمان در نظر بگیرید.

━━━━━━━━━━━━━━

🤔 آیا می‌دانستید؟

بسیاری از مشهورترین جاذبه‌های گردشگری جهان در فهرست میراث جهانی یونسکو ثبت شده‌اند و هر کدام داستان‌ها و رازهای تاریخی جذابی در دل خود دارند.

━━━━━━━━━━━━━━

📚 مطالعه بیشتر

{info['wiki']}

━━━━━━━━━━━━━━

{hashtags}
"""

    # جلوگیری از طول زیاد پیام روبیکا
    if len(post) > 2800:
        post = post[:2700] + "\n\n📚 ادامه:\n" + info["wiki"]

    return post
